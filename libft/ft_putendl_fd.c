#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;
	int	lf;

	i = 0;
	lf = 10;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &lf, 1);
}
